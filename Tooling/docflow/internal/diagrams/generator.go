func renderFrontendPageTagArchitecture(nodes []frontendNode, sharedTags []string) string {
	var b strings.Builder
	b.WriteString(renderDiagramFrontmatter(
		"DIA-FRONTEND-PAGE-TAGS-GEN-001",
		"Generated Frontend Page Tag Architecture",
		[]string{"FEAT-FRONTEND-PAGE-REQS-001", "IMPL-FRONTEND-SHADCN-001", "GOV-FRONTEND-EVOLUTION-001"},
	))
	b.WriteString("# Generated Diagram: Frontend Page Tag Architecture\n\n")
	b.WriteString("```mermaid\ngraph TD\n")
	b.WriteString("  subgraph ShellPages\n")
	for _, node := range nodes {
		if hasTag(node.Tags, "shell") {
			b.WriteString(fmt.Sprintf("    %s[%s]\n", safeID(node.ID), node.ID))
		}
	}
	b.WriteString("  end\n\n")

	b.WriteString("  subgraph ModulePages\n")
	for _, node := range nodes {
		if hasTag(node.Tags, "module") {
			b.WriteString(fmt.Sprintf("    %s[%s]\n", safeID(node.ID), node.ID))
		}
	}
	b.WriteString("  end\n\n")

	tagSet := map[string]bool{}
	for _, tag := range sharedTags {
		tagSet[tag] = true
	}
	for _, node := range nodes {
		for _, tag := range node.Tags {
			tagSet[tag] = true
		}
	}
	tags := mapKeysSorted(tagSet)
	for _, tag := range tags {
		b.WriteString(fmt.Sprintf("  %s[(tag:%s)]\n", safeID("tag-"+tag), tag))
	}
	b.WriteString("\n")

	b.WriteString("  R1[[content+visual requirements]]\n")
	b.WriteString("  G1[[implementation guides]]\n\n")

	for _, node := range nodes {
		nodeID := safeID(node.ID)
		for _, tag := range sortedUnique(node.Tags) {
			b.WriteString(fmt.Sprintf("  %s --> %s\n", nodeID, safeID("tag-"+tag)))
		}
		if node.Links.ContentSpec != "" || node.Links.VisualSpec != "" {
			b.WriteString(fmt.Sprintf("  %s --> R1\n", nodeID))
		}
		if len(node.Links.ImplementationGuides) > 0 {
			b.WriteString(fmt.Sprintf("  %s --> G1\n", nodeID))
		}
	}
	b.WriteString("```\n\n")

	b.WriteString("## Page Linkage\n\n")
	b.WriteString("| Page ID | Content Spec | Visual Spec | Implementation Guides |\n")
	b.WriteString("|---|---|---|---|\n")
	for _, node := range nodes {
		guides := strings.Join(sortedUnique(node.Links.ImplementationGuides), ", ")
		b.WriteString(fmt.Sprintf("| %s | %s | %s | %s |\n", node.ID, node.Links.ContentSpec, node.Links.VisualSpec, guides))
	}

	return b.String()
}

func hasTag(tags []string, expected string) bool {
	for _, tag := range tags {
		if tag == expected {
			return true
		}
	}
	return false
}

func safeID(value string) string {
	replacer := strings.NewReplacer("-", "_", "/", "_", ".", "_", " ", "_")
	return replacer.Replace(value)
}

func unique(values []string) []string {
	seen := map[string]bool{}
	out := make([]string, 0, len(values))
	for _, value := range values {
		if !seen[value] {
			seen[value] = true
			out = append(out, value)
		}
	}
	return out
}

func sortedUnique(values []string) []string {
	out := unique(values)
	sort.Strings(out)
	return out
}

func mapKeysSorted(items map[string]bool) []string {
	out := make([]string, 0, len(items))
	for key := range items {
		out = append(out, key)
	}
	sort.Strings(out)
	return out
}

func renderDiagramFrontmatter(docID, title string, linkedIDs []string) string {
	var b strings.Builder
	b.WriteString("---\n")
	b.WriteString(fmt.Sprintf("doc_id: %s\n", docID))
	b.WriteString(fmt.Sprintf("title: %s\n", title))
	b.WriteString("doc_type: diagram\n")
	b.WriteString("concern: architecture\n")
	b.WriteString("status: accepted\n")
	b.WriteString("phase_scope:\n")
	b.WriteString("- MVP\n")
	b.WriteString("- Pilot\n")
	b.WriteString("- Production\n")
	b.WriteString("- Scale\n")
	b.WriteString("owner_role: principal_architect\n")
	b.WriteString("accountable_role: tech_lead\n")
	b.WriteString("reviewers:\n")
	b.WriteString("- service_owner\n")
	b.WriteString("tags:\n")
	b.WriteString("- ai-development\n")
	b.WriteString("- architecture\n")
	b.WriteString("- diagram\n")
	b.WriteString("- traceability\n")
	b.WriteString("linked_ids")
	if len(linkedIDs) == 0 {
		b.WriteString(": []\n")
	} else {
		b.WriteString(":\n")
		for _, linkedID := range linkedIDs {
			b.WriteString(fmt.Sprintf("- %s\n", linkedID))
		}
	}
	b.WriteString("review_cadence: monthly\n")
	b.WriteString("source_of_truth: true\n")
	b.WriteString("version: '1.0'\n")
	b.WriteString("specificity: platform-shared\n")
	b.WriteString("---\n\n")
	return b.String()
}
